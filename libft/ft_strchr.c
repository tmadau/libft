#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		chr;

	chr = (char)c;
	while (*s)
	{
		if (*s == chr)
			return ((char *)s);
		s++;
	}
	if (*s == chr)
		return ((char *)s);
	return (NULL);
}
