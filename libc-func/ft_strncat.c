#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t		dest_len;
	size_t		i;

	i = 0;
	dest_len = 0;
	while (i < n && s2[i] != '\0')
	{
		s1[dest_len + i] = s2[i];
		i++;
	}
	s1[dest_len + i] = '\0';
	return (s1);
}
